    private void txtNúmeroKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNúmeroKeyTyped
        char entrada = evt.getKeyChar();
        if((entrada <'0' || entrada >'9') && (entrada != '.' || txtNúmero.getText().contains("."))) evt.consume();
    }//GEN-LAST:event_txtNúmeroKeyTyped

